def current_language() -> str:
    """Return the configured UI language code."""
    try:
        language = get_settings().app_language
    except Exception:
        return "en"
    return language if language in SUPPORTED_LANGUAGES else "en"


def tr(text: str, **kwargs: Any) -> str:
    """Translate a UI string and format named placeholders."""
    template = text if current_language() == "ru" else EN.get(text, text)
    if kwargs:
        return template.format(**kwargs)
    return template
